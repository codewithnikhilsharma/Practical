package controlStatement;

public class Switch {
   
	public static void main(String[] args) {
	  
		int no1 = 30;
		switch(no1) {
		
		case 1:
			System.out.println(20);
			break;
			
		case 10:
			System.out.println(40);
			break;
			
		case 3:
				System.out.println(30);
				break;	
		
		case 4:
			System.out.println(40);
			break;
			
		default:System.out.println("Invalid Number");		
				
		}
	}	
}
